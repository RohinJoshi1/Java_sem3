public class loops{
	public static void main(String args[]){
		int i=5;
		//while 
		while(i>0){
			System.out.println("In while");
			i--;
		}
		//for
		for(int j=0;j<5;j++){
			System.out.println("In for");
		}
		//do while
		int k =5;
		do{
			System.out.println("In do while");
			k--;}while(k>0);
	}}
